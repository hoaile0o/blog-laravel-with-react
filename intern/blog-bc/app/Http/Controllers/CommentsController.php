    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request,$post_id)
    {
        $this->validate($request,array(
            'comments'   => 'required|min:5|max:5000'
        ));
        $posts = Post::find($post_id);
        $comment = new Comments();
        $comment->comments = $request->comments;
        $comment->user_id = Auth::user()->id;
        $comment->post()->associate($posts);

        $comment->save();

        Session::flash('comment','Your comment was successfully save !!!');

        $users = User::find($comment->user_id);
        return redirect()->route('posts.detail',compact('users','post_id'));
    }

